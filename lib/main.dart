  String detectSoil(String? state) {
    if (state == null) return "Regional Soil";

    final lowerState = state.toLowerCase();

    if (["haryana", "punjab", "uttar pradesh", "bihar", "west bengal", "assam",
         "odisha", "gujarat", "rajasthan", "madhya pradesh"].contains(lowerState)) {
      return "Alluvial Soil";
    } else if (["tamil nadu", "karnataka", "andhra pradesh", "telangana",
                "odisha", "chhattisgarh", "jharkhand"].contains(lowerState)) {
      return "Red Soil";
    } else if (["maharashtra", "madhya pradesh", "gujarat", "karnataka",
                "andhra pradesh", "telangana", "tamil nadu", "chhattisgarh"].contains(lowerState)) {
      return "Black Soil (Regur)";
    } else if (["kerala", "karnataka", "tamil nadu", "maharashtra",
                "odisha", "west bengal", "assam"].contains(lowerState)) {
      return "Laterite Soil";
    } else if (["rajasthan"].contains(lowerState)) {
      return "Desert Soil";
    } else if (["jammu and kashmir", "himachal pradesh", "uttarakhand", "sikkim",
                "arunachal pradesh", "nagaland", "manipur", "mizoram",
                "tripura", "meghalaya"].contains(lowerState)) {
      return "Mountain Soil";
    } else if (["uttar pradesh", "punjab", "haryana", "rajasthan",
                "gujarat"].contains(lowerState)) {
      return "Saline and Alkaline Soil";
    } else {
      return "Regional Soil";
    }
  }

