class _PremiumNavButtonState extends State<_PremiumNavButton>
    with SingleTickerProviderStateMixin {

  late AnimationController controller;
  late Animation<double> scale;
  late Animation<double> glow;

  @override
  void initState() {
    super.initState();

    controller = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 280),
    );

    scale = Tween<double>(begin: 1.0, end: 1.18).animate(
      CurvedAnimation(parent: controller, curve: Curves.easeOutCubic),
    );

    glow = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(parent: controller, curve: Curves.easeOut),
    );

    if (widget.selected) controller.forward();
  }

  @override
  void didUpdateWidget(covariant _PremiumNavButton oldWidget) {
    super.didUpdateWidget(oldWidget);

    if (widget.selected) {
      controller.forward();
    } else {
      controller.reverse();
    }
  }

  @override
  void dispose() {
    controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    return GestureDetector(
      onTap: () {
        HapticFeedback.lightImpact();
        widget.onTap();
      },
      behavior: HitTestBehavior.translucent,
      child: AnimatedBuilder(
        animation: controller,
        builder: (context, _) {

          final iconColor = Color.lerp(
            isDark ? Colors.white54 : Colors.grey[700],
