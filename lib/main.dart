  Future<Map<String, String>> fetchWeather(double lat, double lon) async {

    final url =
        "https://api.open-meteo.com/v1/forecast"
        "?latitude=$lat"
        "&longitude=$lon"
        "&current_weather=true"
        "&hourly=temperature_2m,relative_humidity_2m,precipitation,wind_speed_10m,"
        "soil_temperature_0cm,soil_temperature_6cm,"
        "soil_moisture_0_to_1cm,soil_moisture_1_to_3cm"
        "&timezone=auto";

    Map<String, String> result = {
      "temperature":    "--",
      "humidity":       "--",
      "windSpeed":      "--",
      "precipitation":  "--",
      "soilTemperature": "--",
      "soilMoisture":   "--",
    };

    try {
      final res  = await http.get(Uri.parse(url));
      final data = jsonDecode(res.body);

      print("Weather API status: ${res.statusCode}");
      print("Weather API keys: ${data.keys.toList()}");

      final currentWeather = data["current_weather"];
      final hourly         = data["hourly"];

      if (currentWeather == null) {
        print("current_weather is null");
        return result;
      }
      if (hourly == null) {
        print("hourly is null — API may have rejected parameters");
        return result;
      }

      final List times = hourly["time"] ?? [];
      if (times.isEmpty) {
        print("hourly time list is empty");
        return result;
      }

      final DateTime now = DateTime.now();
      int currentIndex = 0;
      int smallestDiff = 999999;
      for (int i = 0; i < times.length; i++) {
        final DateTime forecastTime = DateTime.parse(times[i]);
        final int diff = forecastTime.difference(now).inMinutes.abs();
        if (diff < smallestDiff) {
          smallestDiff = diff;
          currentIndex = i;
        }
      }

      print("Using hourly index: $currentIndex for time: ${times[currentIndex]}");

      // Pick whichever soil key the API returned
      final soilTempList    = hourly["soil_temperature_0cm"]   ?? hourly["soil_temperature_6cm"];
      final soilMoistList   = hourly["soil_moisture_0_to_1cm"] ?? hourly["soil_moisture_1_to_3cm"];

      final double temperatureVal    = (currentWeather["temperature"]  as num?)?.toDouble() ?? 0.0;
      final double windSpeedVal      = (currentWeather["windspeed"]    as num?)?.toDouble() ?? 0.0;
      final double humidityVal       = (hourly["relative_humidity_2m"]?[currentIndex] as num?)?.toDouble() ?? 0.0;
      final double precipitationVal  = (hourly["precipitation"]?[currentIndex]        as num?)?.toDouble() ?? 0.0;
      final double soilTempVal       = (soilTempList?[currentIndex]  as num?)?.toDouble() ?? 0.0;
      final double soilMoistureVal   = (soilMoistList?[currentIndex] as num?)?.toDouble() ?? 0.0;

      print("soil_temp: $soilTempVal  soil_moisture: $soilMoistureVal");

      result["temperature"]     = "${temperatureVal}°C";
      result["humidity"]        = "${humidityVal.toInt()}%";
      result["windSpeed"]       = "${windSpeedVal} m/s";
      result["precipitation"]   = "${precipitationVal} mm";
      result["soilTemperature"] = "${soilTempVal}°C";
      result["soilMoisture"]    = "${soilMoistureVal.toStringAsFixed(3)} m³/m³";

    } catch (e) {
      print("Error fetching weather: $e");
    }
    return result;
  }

