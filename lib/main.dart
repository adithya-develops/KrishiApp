  Future<void> detectLocation({
    String? state,
    String? district,
    String? town,
  }) async {
    HapticFeedback.mediumImpact();

    setState(() {
      loading = true;
      _isManualLocationCardLoading = (state != null);
    });

    try {
      Position? pos;
      String currentSoil = "Regional Soil";
      String currentLocation = "Tap to detect";
      Map<String, String> weatherDetails;

      if (state == null) {
        // ── Auto-detect mode ──
        final permission = await Geolocator.requestPermission();
        if (permission == LocationPermission.denied) {
          setState(() {
            loading = false;
            location = "Location permission denied";
            _isManualLocationCardLoading = false;
          });
          return;
        }
        pos = await Geolocator.getCurrentPosition();
        final placemarks = await placemarkFromCoordinates(pos.latitude, pos.longitude);
        final place = placemarks.first;
        currentSoil = detectSoil(place.administrativeArea);
        currentLocation = "${place.locality}, ${place.administrativeArea}";
        weatherDetails = await fetchWeather(pos.latitude, pos.longitude);
      } else {
        // ── Manual entry mode ──
        final fullAddress = "$town, $district, $state, India";
        final locations = await locationFromAddress(fullAddress);
        if (locations.isEmpty) throw Exception("Location not found.");

        final firstLocation = locations.first;
        final lat = firstLocation.latitude;
        final lon = firstLocation.longitude;

        // Validate: must be within India's bounding box
        final bool insideIndia =
            lat >= 6.0 && lat <= 37.5 && lon >= 68.0 && lon <= 98.0;
        if (!insideIndia) throw Exception("Location not found.");

        // Reverse-geocode and cross-check
        final placemarks = await placemarkFromCoordinates(lat, lon);
        if (placemarks.isEmpty) throw Exception("Location not found.");
        final placemark = placemarks.first;

        final returnedInfo = [
          placemark.administrativeArea,
          placemark.subAdministrativeArea,
          placemark.locality,
          placemark.subLocality,
          placemark.country,
        ].map((s) => (s ?? "").toLowerCase()).join(" ");

        final enteredState    = state.toLowerCase().trim();
        final enteredDistrict = district!.toLowerCase().trim();
        final enteredTown     = town!.toLowerCase().trim();

        bool stateMatches    = enteredState.split(" ").any((w) => w.length > 2 && returnedInfo.contains(w));
        bool districtMatches = enteredDistrict.split(" ").any((w) => w.length > 2 && returnedInfo.contains(w));
        bool townMatches     = enteredTown.split(" ").any((w) => w.length > 2 && returnedInfo.contains(w));

        int matchCount = (stateMatches ? 1 : 0) + (districtMatches ? 1 : 0) + (townMatches ? 1 : 0);
        if (matchCount < 1) throw Exception("Location not found.");

        pos = Position(
          latitude: lat,
          longitude: lon,
          timestamp: DateTime.now(),
          accuracy: 0.0,
          altitude: 0.0,
          altitudeAccuracy: 0.0,
          heading: 0.0,
          headingAccuracy: 0.0,
          speed: 0.0,
          speedAccuracy: 0.0,
        );
        currentSoil = detectSoil(state);
        currentLocation = "$town, $district, $state";
        weatherDetails = await fetchWeather(pos.latitude, pos.longitude);
      }

      final data = await Storage.loadAll();
      await Storage.saveAll(
        currentLocation,
        weatherDetails["temperature"]!,
        weatherDetails["humidity"]!,
        weatherDetails["windSpeed"]!,
        weatherDetails["precipitation"]!,
        weatherDetails["soilTemperature"]!,
        weatherDetails["soilMoisture"]!,
        currentSoil,
        data["dark"],
        _isManualLocationEntry,
        _manualStateController.text,
        _manualDistrictController.text,
        _manualTownController.text,
        false,  // reset manual soil override when location changes
        "",     // clear manualSoilType so soil card reflects new auto-detected soil
      );

      HapticFeedback.heavyImpact();

      setState(() {
        loading = false;
        _isManualLocationCardLoading = false;
        this.location       = currentLocation;
        this.temperature    = weatherDetails["temperature"]!;
        this.humidity       = weatherDetails["humidity"]!;
        this.windSpeed      = weatherDetails["windSpeed"]!;
        this.precipitation  = weatherDetails["precipitation"]!;
        this.soilTemperature = weatherDetails["soilTemperature"]!;
        this.soilMoisture   = weatherDetails["soilMoisture"]!;
        this.soil           = currentSoil;
      });

      anim.forward(from: 0);
    } catch (e) {
      print("Error detecting location: $e");
      setState(() {
        loading = false;
        _isManualLocationCardLoading = false;
        location = "Tap to detect";
      });
