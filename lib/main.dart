class SoilScreen extends StatefulWidget {
  const SoilScreen({super.key});

  @override
  State<SoilScreen> createState() => _SoilScreenState();
}

class _SoilScreenState extends State<SoilScreen>
    with SingleTickerProviderStateMixin {
  late AnimationController controller;
  Map<String, dynamic> soilData = {};
  String _currentSoilType = "--"; // Initialize with a placeholder

  @override
  void initState() {
    super.initState();
    controller = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 350),
    );
    loadSoil();
  }

  @override
  void didChangeDependencies() {
    super.didChangeDependencies();
    // Re-translate soil data whenever language changes
    if (_currentSoilType != "--") { // Only update if a soil type is set
      final lang = LangProvider.of(context).langCode;
      soilData = SoilDatabase.getFullSoilProfile(_currentSoilType, langCode: lang);
    }
  }

  @override
  void dispose() {
    controller.dispose();
    super.dispose();
  }

  void loadSoil() async {
    final saved = await Storage.loadAll();
    String autoSoil = saved["soil"] ?? "--";
    String manualSoilType = saved["manualSoilType"] ?? "";
    bool wasManual = saved["isManualSoilSelection"] ?? false;

    String resolvedSoilType;

    // Logic to determine the current soil type
    if (wasManual && manualSoilType.isNotEmpty && manualSoilType != "--") {
      resolvedSoilType = manualSoilType;
    } else if (autoSoil.isNotEmpty && autoSoil != "--" && autoSoil != "Regional Soil") {
      resolvedSoilType = autoSoil;
    } else {
      // Fallback if neither auto-detected nor manual soil is valid/present
      // For now, let's default to "Alluvial Soil" as a safe, common choice.
      // You might want to display a prompt to the user or a "Detect Location" button instead.
      resolvedSoilType = "Alluvial Soil"; // Default to a valid soil type
      // Also ensure that if we fall back, manual selection is off
      if (wasManual) { // Only update if it was mistakenly set as manual when it shouldn't be
         await Storage.saveAll(
            saved["location"], saved["temperature"], saved["humidity"],
            saved["windSpeed"], saved["precipitation"], saved["soilTemperature"],
            saved["soilMoisture"], saved["soil"], saved["dark"],
            saved["isManualLocationEntry"], saved["manualState"],
            saved["manualDistrict"], saved["manualTown"],
            false, // Reset isManualSoilSelection
            "",    // Clear manualSoilType
          );
      }
    }

    if (!mounted) return;

    final lang = LangProvider.of(context).langCode;
    _currentSoilType = resolvedSoilType;
    soilData = SoilDatabase.getFullSoilProfile(_currentSoilType, langCode: lang);
    controller.forward(from: 0);
    setState(() {});
  }

  Future<void> _onSoilChanged(String? newSoilType) async {
    if (newSoilType == null || newSoilType == "--") return; // Prevent setting to invalid types

    final data = await Storage.loadAll();
    await Storage.saveAll(
      data["location"], data["temperature"], data["humidity"],
