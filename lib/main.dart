class Storage {
  
  static Future<void> saveAll(
      String location,
      String temperature,
      String humidity,
      String windSpeed,
      String precipitation,
      String soilTemperature,
      String soilMoisture,
      String soil,
      bool darkMode,
      bool isManualLocationEntry,
      String manualState,
      String manualDistrict,
      String manualTown,
      bool isManualSoilSelection,
      String manualSoilType,
      ) async {

    final prefs = await SharedPreferences.getInstance();

    prefs.setString("location", location);
    prefs.setString("temperature", temperature);
    prefs.setString("humidity", humidity);
    prefs.setString("windSpeed", windSpeed);
    prefs.setString("precipitation", precipitation);
    prefs.setString("soilTemperature", soilTemperature);
    prefs.setString("soilMoisture", soilMoisture);
    prefs.setString("soil", soil);
    prefs.setBool("dark", darkMode);
    prefs.setBool("isManualLocationEntry", isManualLocationEntry);
    prefs.setString("manualState", manualState);
    prefs.setString("manualDistrict", manualDistrict);
    prefs.setString("manualTown", manualTown);
    prefs.setBool("isManualSoilSelection", isManualSoilSelection);
    prefs.setString("manualSoilType", manualSoilType);
  }

  static Future<Map<String, dynamic>> loadAll() async {

    final prefs = await SharedPreferences.getInstance();

    return {
      "location":      prefs.getString("location") ?? "Tap to detect",
      "temperature":   prefs.getString("temperature") ?? "--",
      "humidity":      prefs.getString("humidity") ?? "--",
      "windSpeed":     prefs.getString("windSpeed") ?? "--",
      "precipitation": prefs.getString("precipitation") ?? "--",
      "soilTemperature": prefs.getString("soilTemperature") ?? "--",
      "soilMoisture":  prefs.getString("soilMoisture") ?? "--",
      "soil":          prefs.getString("soil") ?? "--",
      "dark":          prefs.getBool("dark") ?? true,
      "isManualLocationEntry": prefs.getBool("isManualLocationEntry") ?? false,
      "manualState":   prefs.getString("manualState") ?? "",
      "manualDistrict": prefs.getString("manualDistrict") ?? "",
      "manualTown":    prefs.getString("manualTown") ?? "",
      "isManualSoilSelection": prefs.getBool("isManualSoilSelection") ?? false,
      "manualSoilType": prefs.getString("manualSoilType") ?? "",
    };
  }
}

