  final IconData? icon;
  final Color accentColor;
  final Widget? suffixIcon;

  const PremiumGlassTextField({
    super.key,
    required this.controller,
    required this.labelText,
    this.keyboardType = TextInputType.text,
    this.obscureText = false,
    this.icon,
    this.accentColor = const Color(0xFF4FC3F7),
    this.suffixIcon,
  });

  @override
  State<PremiumGlassTextField> createState() => _PremiumGlassTextFieldState();
}

class _PremiumGlassTextFieldState extends State<PremiumGlassTextField>
    with SingleTickerProviderStateMixin {
  final FocusNode _focus = FocusNode();
  bool _focused = false;
  late AnimationController _focusAnim;
  late Animation<double> _glowAnim;

  @override
  void initState() {
    super.initState();
    _focusAnim = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 280),
    );
    _glowAnim = CurvedAnimation(parent: _focusAnim, curve: Curves.easeOut);

    _focus.addListener(() {
      setState(() => _focused = _focus.hasFocus);
      if (_focus.hasFocus) {
        _focusAnim.forward();
      } else {
        _focusAnim.reverse();
      }
    });
  }

  @override
  void dispose() {
    _focus.dispose();
    _focusAnim.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return AnimatedBuilder(
      animation: _glowAnim,
      builder: (context, child) {
        return Container(
          decoration: BoxDecoration(
            borderRadius: BorderRadius.circular(16),
            color: Colors.white.withOpacity(_focused ? 0.10 : 0.06),
            border: Border.all(
              color: _focused
                  ? widget.accentColor.withOpacity(0.85)
                  : Colors.white.withOpacity(0.18),
              width: _focused ? 1.6 : 1.0,
            ),
            boxShadow: _focused
                ? [
                    BoxShadow(
                      color: widget.accentColor.withOpacity(0.28 * _glowAnim.value),
                      blurRadius: 20,
                      spreadRadius: 0,
                    ),
                  ]
                : [],
          ),
          child: TextField(
            controller: widget.controller,
            focusNode: _focus,
            keyboardType: widget.keyboardType,
            obscureText: widget.obscureText,
            style: GoogleFonts.dmSans(
              color: Colors.white,
              fontSize: 14.5,
              fontWeight: FontWeight.w500,
            ),
            decoration: InputDecoration(
              contentPadding:
                  const EdgeInsets.symmetric(horizontal: 16, vertical: 16),
              prefixIcon: widget.icon != null
                  ? Padding(
                      padding: const EdgeInsets.only(left: 14, right: 10),
                      child: Icon(
                        widget.icon,
                        size: 18,
                        color: _focused
                            ? widget.accentColor
                            : Colors.white.withOpacity(0.40),
                      ),
                    )
                  : null,
              prefixIconConstraints:
                  const BoxConstraints(minWidth: 0, minHeight: 0),
              suffixIcon: widget.suffixIcon != null
                  ? Padding(
                      padding: const EdgeInsets.only(right: 14),
                      child: widget.suffixIcon,
                    )
