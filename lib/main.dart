/// LANGUAGE STORAGE (separate from main Storage to keep it simple)
////////////////////////////////////////////////////////////

class LangStorage {
  static const _key = "appLanguage";

  static Future<void> save(String langCode) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_key, langCode);
  }

  static Future<String> load() async {
    final prefs = await SharedPreferences.getInstance();
    return prefs.getString(_key) ?? "en";
  }
}

