  final String langCode;
  final VoidCallback onChangeLanguage;

  const LangProvider({
    super.key,
    required this.langCode,
    required this.onChangeLanguage,
    required super.child,
  });

  static LangProvider of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<LangProvider>()!;
  }

  AppLocalizations get l => AppLocalizations(langCode);

  @override
  bool updateShouldNotify(LangProvider old) => old.langCode != langCode;
}



