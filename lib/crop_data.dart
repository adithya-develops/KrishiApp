  /// Returns the localised display name for an English crop key.
  /// Safe: if [cropName] is already a localised string that isn't an English
  /// key, we first convert it to English, then look up the display name.
  static String getDisplayName(String cropName, String langCode) {
    // 1. Try direct lookup (cropName is already an English key)
    if (localizedNames.containsKey(cropName)) {
      return localizedNames[cropName]![langCode] ??
             localizedNames[cropName]!["en"] ??
             cropName;
    }
    // 2. Convert from localised → English → localised
    final english = getEnglishName(cropName);
    return localizedNames[english]?[langCode] ??
           localizedNames[english]?["en"] ??
           cropName;
  }

  /// Converts any spelling (English, Hindi, Tamil, …) to the English DB key.
  static String getEnglishName(String name) {
    final n = name.trim();

    // Step 1: direct alias hit
    if (aliases.containsKey(n)) return aliases[n]!;

    // Step 2: scan all localizedNames values
    for (final entry in localizedNames.entries) {
      for (final val in entry.value.values) {
        if (val.trim() == n) return entry.key;
      }
    }

    // Step 3: case-insensitive English alias fallback
    final lower = n.toLowerCase();
    for (final key in aliases.keys) {
      if (key.toLowerCase() == lower) return aliases[key]!;
    }

    // Step 4: assume already English
    return n;
  }
